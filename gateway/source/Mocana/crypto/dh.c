static MSTATUS
DH_generateY(randomContext *pRandomContext, ubyte4 lengthY, vlong** ppY, vlong** ppVlongQueue)
{
    ubyte* yBuf = 0;
    MSTATUS status;

    /* MALLOC but for 24 bytes could use the stack */
    if (NULL == (yBuf = MALLOC(lengthY)))
    {
        status = ERR_MEM_ALLOC_FAIL;
        goto exit;
    }

    if (OK > (status = RANDOM_numberGenerator(pRandomContext, yBuf, lengthY)))
        goto exit;

    if (OK > (status = VLONG_vlongFromByteString(yBuf, lengthY, ppY, ppVlongQueue)))
        goto exit;

    if(NULL != ppY)
        DEBUG_RELABEL_MEMORY(*ppY);

exit:
    if (yBuf)
        FREE(yBuf);

    return status;
}


