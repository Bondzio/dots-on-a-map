extern MSTATUS
DH_getP(ubyte4 groupNum, vlong **ppRetP)
{
    MSTATUS status;

    /* Compute P */
    if (DH_GROUP_1 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group1, sizeof(group1), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_2 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group2, sizeof(group2), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_5 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group5, sizeof(group5), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_14 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group14, sizeof(group14), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_15 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group15, sizeof(group15), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_16 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group16, sizeof(group16), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_17 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group17, sizeof(group17), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_18 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group18, sizeof(group18), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else if (DH_GROUP_24 == groupNum)
    {
        if (OK > (status = VLONG_vlongFromByteString(group24, sizeof(group24), ppRetP, NULL)))
        {
            goto exit;
        }
    }
    else
    {
        status = ERR_CRYPTO_DH_UNSUPPORTED_GROUP;
        goto exit;
    }

    if(NULL != ppRetP)
        DEBUG_RELABEL_MEMORY(*ppRetP);

exit:
    return status;
}


