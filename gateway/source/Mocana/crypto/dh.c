extern MSTATUS
DH_getG(ubyte4 groupNum, vlong **ppRetG, vlong **ppVlongQueue)
{
    MSTATUS status;

    if (DH_GROUP_24 == groupNum)
    {
        status = VLONG_vlongFromByteString(group24_g, sizeof(group24_g), ppRetG, ppVlongQueue);
    }
    else
    {
        status = VLONG_makeVlongFromUnsignedValue(2, ppRetG, ppVlongQueue);
    }

    return status;
}


