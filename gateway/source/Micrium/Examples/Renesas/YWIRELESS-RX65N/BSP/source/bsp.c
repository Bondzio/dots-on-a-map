/*
*********************************************************************************************************
*                                               BSP_Init()
*
* Description : Initialize the Board Support Package (BSP).
*
* Argument(s) : none.
*
* Return(s)   : none.
*
* Caller(s)   : Application.
*
* Note(s)     : (1) This function SHOULD be called before any other BSP function is called.
*********************************************************************************************************
*/

void  BSP_Init (void)
{
    BSP_OSTickInit();

#if BSP_CFG_LED_EN > 0
    BSP_LED_Init();                                             /* Init LEDs.                                           */
#endif

#if BSP_CFG_PB_EN > 0
    BSP_PB_Init();
#endif
}


/*
*********************************************************************************************************
*                                          BSP_SystemInit()
*
* Description : Initializes the third party library(If necessary) and sets the CPU clock frequency.
*
* Argument(s) : none.
*
* Return(s)   : none.
*
* Caller(s)   : none.
*
* Note(s)     : none.
*********************************************************************************************************
*/

void  BSP_SystemInit (void)
{
    CPU_IntDis();                                               /* Disable all interrupts.                              */
    BSP_IntVectSet(27, (CPU_FNCT_VOID)OSCtxSwISR);              /* Setup kernel context switch                          */
    BSP_ClkCfg();
}
